<h4>Icon Type Icon Preset</h4>
<p>If this option is selected you can preview and apply with different types of pre-defined icon of your choice for the button.</p>

<h4>Icon</h4>
<p>There are eleven types of pre-defined Font Awesome icons you can choose from.</p>

<h4>Icon Size</h4>
<p>Icon sizes you can choose are:</p>
<ul>
  <li>lg</li>
  <li>2x</li>
  <li>3x</li>
  <li>4x</li>
  <li>5x</li>
</ul>

<h4>Background Width</h4>
<p>Width of the button's background.</p>

<h4>Background Height</h4>
<p>Height of the button's background.</p>

<h4>Icon Margin Top</h4>
<p>Margin top of the icon from the background.</p>