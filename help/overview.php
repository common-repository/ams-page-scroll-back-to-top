<p>This screen is used for managing AMS Page Scroll Back To Top button.</p>

<p>From this screen you can:</p>
<ul>
	<li>Select preset image for the button and customization.</li>
	<li>Select preset icon for the button and customization.</li>
	<li>Put custom text for the button and customization.</li>
	<li>Select custom image for the button and customization.</li>
</ul>

<p>Options in the 'Appearance' tab changes according to the selection of button type on this page.</p>

